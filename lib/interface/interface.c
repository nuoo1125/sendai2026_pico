// --- SSD1306 基本制御関数 ---
void ssd1306_send_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(gyro_i2c, SSD1306_ADDR, buf, 2, false);
}

void ssd1306_init() {
    i2c_init(gyro_i2c, 400000);
    gpio_set_function(gyro_sda, GPIO_FUNC_I2C);
    gpio_set_function(gyro_scl, GPIO_FUNC_I2C);
    gpio_pull_up(gyro_sda);
    gpio_pull_up(gyro_scl);
    uint8_t init_cmds[] = {
        0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40,
        0x8D, 0x14, 0x20, 0x00, 0xA1, 0xC8, 0xDA, 0x12,
        0x81, 0xCF, 0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF
    };
    for (size_t i = 0; i < sizeof(init_cmds); i++) {
        ssd1306_send_cmd(init_cmds[i]);
    }
}

void oled_clear() {
    uint8_t buf[129];
    memset(buf, 0, sizeof(buf));
    buf[0] = 0x40; 
    for (int i = 0; i < 8; i++) {
        ssd1306_send_cmd(0xB0 + i);
        ssd1306_send_cmd(0x00);
        ssd1306_send_cmd(0x10);
        i2c_write_blocking(gyro_i2c, SSD1306_ADDR, buf, 129, false);
    }
}

void oled_putchar(char c) {
    // 表示可能なASCII範囲外（32未満または126より大きい）はスペースに置き換え
    if (c < 32 || c > 126) c = 32;
    
    uint8_t buf[7] = {0x40}; 
    // font5x7配列から該当文字のパターンをコピー (c-32が配列のインデックス)
    memcpy(&buf[1], font5x7[c - 32], 5);
    buf[6] = 0x00; // 文字間の1ピクセル隙間

    i2c_write_blocking(gyro_i2c, SSD1306_ADDR, buf, 7, false);
}

void oled_print(const char *str, uint8_t page, uint8_t col) {
    ssd1306_send_cmd(0xB0 + page);
    ssd1306_send_cmd(0x00 | (col & 0x0F));
    ssd1306_send_cmd(0x10 | (col >> 4));
    while (*str) {
        oled_putchar(*str++);
    }
}